const fs = require("fs");
const path = require("path");

const rootDir = path.resolve(__dirname, "..");
const galleryDir = path.join(rootDir, "public", "gallery");
const outputFile = path.join(rootDir, "src", "data", "gallery.js");
const supportedImageExtensions = new Set([".jpg", ".jpeg", ".png", ".webp", ".avif"]);
const supportedVideoExtensions = new Set([".mp4", ".webm", ".mov"]);
const supportedExtensions = new Set([
  ...supportedImageExtensions,
  ...supportedVideoExtensions,
]);

const slugify = (value) => value
  .toLowerCase()
  .normalize("NFKD")
  .replace(/[\u0300-\u036f]/g, "")
  .replace(/[^a-z0-9]+/g, "-")
  .replace(/^-+|-+$/g, "");

const titleFromFolder = (folderName) => folderName
  .replace(/[-_]+/g, " ")
  .replace(/\s+/g, " ")
  .trim()
  .replace(/\b\w/g, (letter) => letter.toUpperCase());

const titleFromFile = (fileName) => path.basename(fileName, path.extname(fileName))
  .replace(/[-_]+/g, " ")
  .replace(/\s+/g, " ")
  .trim();

const ensureOutputDir = () => {
  fs.mkdirSync(path.dirname(outputFile), { recursive: true });
};

const readAlbums = () => {
  if (!fs.existsSync(galleryDir)) return [];

  return fs.readdirSync(galleryDir, { withFileTypes: true })
    .filter((entry) => entry.isDirectory() && !entry.name.startsWith("."))
    .sort((a, b) => a.name.localeCompare(b.name))
    .map((entry) => {
      const slug = slugify(entry.name);
      const title = titleFromFolder(entry.name);
      const folder = path.join(galleryDir, entry.name);
      const media = fs.readdirSync(folder, { withFileTypes: true })
        .filter((file) => file.isFile() && supportedExtensions.has(path.extname(file.name).toLowerCase()))
        .map((file) => file.name)
        .sort((a, b) => a.localeCompare(b, undefined, { numeric: true }))
        .map((fileName, index) => {
          const extension = path.extname(fileName).toLowerCase();
          const src = `./public/gallery/${entry.name}/${fileName}`;
          const stats = fs.statSync(path.join(folder, fileName));
          return {
            id: `${slug}-${slugify(path.basename(fileName, extension)) || index}`,
            type: supportedVideoExtensions.has(extension) ? "video" : "image",
            src,
            fileName,
            title: titleFromFile(fileName),
            alt: `${title} media ${index + 1}`,
            order: index,
            modifiedAt: stats.mtime.toISOString(),
            modifiedAtMs: Math.round(stats.mtimeMs),
          };
        });
      const imageEntries = media.filter((item) => item.type === "image");

      return {
        id: slug,
        title,
        slug,
        folderPath: `./public/gallery/${entry.name}`,
        coverImage: imageEntries[0]?.src || media[0]?.src || "",
        images: imageEntries.map((image) => image.src),
        imageEntries,
        media,
      };
    });
};

const writeDataFile = (albums) => {
  const data = `// Generated by scripts/generate-gallery-data.js. Do not edit manually.
export const galleryAlbums = ${JSON.stringify(albums, null, 2)};
`;
  fs.writeFileSync(outputFile, data);
};

ensureOutputDir();
const albums = readAlbums();
writeDataFile(albums);
console.log(`Generated ${albums.length} gallery album${albums.length === 1 ? "" : "s"} in ${path.relative(rootDir, outputFile)}.`);
